export function applyPreset(currentState, presetId) {
  const preset = PRESETS.find((entry) => entry.id === presetId);

  if (!preset) {
    return currentState;
  }

  return {
    ...currentState,
    profile: {
      ...currentState.profile,
      ...preset.profile,
      name: currentState.profile.name,
      username: currentState.profile.username,
      quote: currentState.profile.quote,
    },
    appearance: {
      ...currentState.appearance,
      ...preset.appearance,
    },
    widgets: {
      ...currentState.widgets,
      ...preset.widgets,
    },
    tech: {
      ...currentState.tech,
      languages: [...(preset.tech.languages || [])],
      frontend: [...(preset.tech.frontend || [])],
      backend: [...(preset.tech.backend || [])],
      devops: [...(preset.tech.devops || [])],
      ai: [...(preset.tech.ai || [])],
    },
  };
}
